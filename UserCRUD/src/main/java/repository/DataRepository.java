package repository;

import java.util.List;
import java.util.Optional;

/**
 * Базовый интерфейс репозитория для управления жизненным циклом сущностей.
 * Определяет стандартный набор CRUD операций (Create, Read, Update, Delete).
 *
 * @param <T>  тип сущности, с которой работает репозиторий
 * @param <ID> тип уникального идентификатора сущности
 */
public interface DataRepository<T, ID> {

    /**
     * Сохраняет состояние сущности в базе данных.
     * Может использоваться как для создания новой записи, так и для обновления существующей.
     *
     * @param entity сущность для сохранения
     * @return сохраненная сущность (включая сгенерированный базой данных ID)
     */
    T save(T entity);

    /**
     * Массово создает новые записи в базе данных.
     * Используется для эффективной вставки списка сущностей за одну операцию.
     *
     * @param entities список сущностей для создания
     * @return список созданных сущностей с присвоенными идентификаторами
     */
    public List<T> createAll(List<T> entities);

    /**
     * Выполняет поиск сущности по её уникальному идентификатору.
     *
     * @param id уникальный идентификатор сущности
     * @return {@link Optional}, содержащий найденную сущность, или пустой Optional, если запись не найдена
     */
    Optional<T> findById(ID id);

    /**
     * Извлекает все записи данной сущности из базы данных.
     *
     * @return список всех найденных сущностей; если данных нет — возвращается пустой список
     */
    List<T> findAll();

    /**
     * Удаляет сущность из базы данных по её идентификатору.
     *
     * @param id идентификатор сущности, предназначенной для удаления
     * @return удаленная сущность
     * @throws RuntimeException (или специализированное исключение) если сущность с таким ID не существует
     */
    T deleteById(ID id);
}

